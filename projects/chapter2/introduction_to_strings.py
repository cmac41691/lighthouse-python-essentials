# Chapter 2 - Introduction to Strings

# Basic string examples
greeting = "Hello, Python learner!"
print(greeting)

# Single quotes also work
alt_greeting = 'Hello again!' 
print(alt_greeting)

# String concatenation
first_name = "Ada"
last_name = "Lovelace"
full_name = first_name + " " + last_name
print("Full Name:", full_name)

# String repetition
echo = "Python! " * 3
print(echo)
